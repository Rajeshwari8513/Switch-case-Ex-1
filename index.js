let dayOfWeek = 6;
let dayName;

switch (dayOfWeek){
    case 0 :
        dayName = "Sunday";
        console.log("It's a week-end")
        break;
    case 1 :
        dayName = "Monday";
        console.log("It's a weekday")
        break;
    case 2 :
        dayName = "Tuesday";
        console.log("It's a weekday")
        break;
    case 3 :
        dayName = "Wednesday";
        console.log("It's a weekday")
        break;
    case 4 :
        dayName = "Thrusday";
        console.log("It's a weekday")
        break;
    case 5 :
        dayName = "Friday";
        console.log("It's a weekday")
        break;
    case 6 :
        dayName = "Saturday";
        console.log("It's a week-end")
        break;
    default :
    console.log("Invalid day")
}